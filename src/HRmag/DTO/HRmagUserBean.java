package HRmag.DTO;

public class HRmagUserBean {
	private String empNo;
	private String empName;
	private String deptCode;
	private String empPosition;
	private String empAddress;
	private String empContact;
	private String empProfileImg;
	private String empHireDate;
	public String getEmpNo() {
		return empNo;
	}
	public void setEmpNo(String empNo) {
		this.empNo = empNo;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public String getDeptCode() {
		return deptCode;
	}
	public void setDeptCode(String deptCode) {
		this.deptCode = deptCode;
	}
	public String getEmpPosition() {
		return empPosition;
	}
	public void setEmpPosition(String empPosition) {
		this.empPosition = empPosition;
	}
	public String getEmpAddress() {
		return empAddress;
	}
	public void setEmpAddress(String empAddress) {
		this.empAddress = empAddress;
	}
	public String getEmpContact() {
		return empContact;
	}
	public void setEmpContact(String empContact) {
		this.empContact = empContact;
	}
	public String getEmpProfileImg() {
		return empProfileImg;
	}
	public void setEmpProfileImg(String empProfileImg) {
		this.empProfileImg = empProfileImg;
	}
	public String getEmpHireDate() {
		return empHireDate;
	}
	public void setEmpHireDate(String empHireDate) {
		this.empHireDate = empHireDate;
	}
}
